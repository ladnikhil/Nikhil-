public class RelationalOperator{
public static void main(String []args){

int i = 65;
char j = 'A';


boolean b = i == j;
System.out.println("for == "+b);

b = i!=j;
System.out.println("for != "+b);

b = i<j;
System.out.println("for < "+b);


b = i>j;
System.out.println("for > "+b);


b = i<=j;
System.out.println("for <= "+b);


b = i>=j;
System.out.println("for >= "+b);



}
}