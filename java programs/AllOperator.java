public class AllOperator{
public static void main(String []args){

int a = 20;
int b = 30;

// arithmetic operators

int c = a + b;
System.out.println("arithmetic operators is "+c);

// assignment operators

c += b;
System.out.println("assignment operator is "+c);

// preincrement operators

c = ++b;
System.out.println("preincrement operator is "+c);

// relational operators


boolean d = b>a;
System.out.println("relational operator is "+d);

// logical operators

boolean m = true;
boolean n = true;

boolean e = (m&&n);
System.out.println("logical operator is "+e);




}
}