public class Forloop{
public static void main(String []args){

for(int i = 1; i<=10; i++)
{

System.out.println("i = "+i);

}
System.out.println("code outside for loop ");





								// table of 27


for(int i = 1; i<=10; i++)
{


System.out.println("27*"+i+"="+(27*i));
 
}
System.out.println("code outside for loop ");


								// for infinite loop                  

for( ; ; )           // it will infinitely loop
{
System.out.println(i);
i++;
}
System.out.println("code outside for loop ");                    // it will not get executed as we have infinite loop






}
}
