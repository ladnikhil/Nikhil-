 public class ArithmeticOperators{

public static void main(String []args){

int i = 10;
int j = 20;

int addition = i + j;
System.out.println("addition ="+addition);                                                 // addition
 
int substraction = i - j;
System.out.println("substraction = "+substraction);                         // substraction

int multiplication = i * j;
System.out.println("multiplication = "+multiplication);                      // multiplication

int division = i / j;
System.out.println("division = "+division);				// division	

int modulus = i % j;
System.out.println("modulus = "+modulus);				// modulus


/// using char variable

char a = 'n';
int b = 20;
int result = a + b;

System.out.println("addition of character= "+result);

result = a - b;							// reinitialization of variable
System.out.println("substraction of character = "+result);

result = a * b;							// reinitialization of variable
System.out.println("multiplication of character = "+result);

result = a / b;							// reinitialization of variable
System.out.println("division of character = "+result);

result = a % b;							// reinitialization of variable
System.out.println("modulus of character = "+result);

}
}
