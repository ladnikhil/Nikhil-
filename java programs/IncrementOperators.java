public class IncrementOperators{
public static void main(String []args){

// post increment

int i = 10;
int j = i++;   // substitute here we only asiign to another value so value of j is 10 
System.out.println(j);   //utilization

// pre increment

j = ++i;              // substitute  here calue of j is 11 
System.out.println(j);   // utilization


// post decrement

int a = 10;
int b = a--;
System.out.println(b); 

b = --a;              // substitute  here calue of j is 11 
System.out.println(b);   // utilization


}
}